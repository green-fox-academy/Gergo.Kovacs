#include <iostream>

int main(int argc, char* args[]) {

    // Swap the values of the variables
    int a = 123; int b = 526; int c;

    std::cout<< a << std::endl;
    std::cout << b << std::endl;


    c = a; a = b;
    std::cout<<(b)<<std::endl;
    std::cout<<(c)<<std::endl;






    return 0;
}